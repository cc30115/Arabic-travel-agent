/**
 * Automatically applies image links to elements with data-asset attribute.
 * Usage: <img data-asset="hero_bg"> or <div data-asset="hero_bg"> (for background)
 */
function applyImageLinks() {
    const elements = document.querySelectorAll('[data-asset]');
    elements.forEach(el => {
        const key = el.getAttribute('data-asset');
        const link = window.imageLinks[key];
        if (link) {
            if (el.tagName === 'IMG') {
                el.src = link;
            } else {
                el.style.backgroundImage = `url(${link})`;
            }
        }
    });
}

// Run when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', applyImageLinks);
} else {
    applyImageLinks();
}
